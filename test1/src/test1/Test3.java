package test1;

public class Test3 {

	public static void main(String[] args) {

		// 변수 선언
		int x;
		int y;

		x = 100;
		y = 50;
		x = 200;

		System.out.println(x + y); // x+y = 250

		y = 100;

		System.out.println(x + y); // x+y = 300

		// 변수 선언 연습
		// 정수형
		int a;
		long b;
		short c;
		byte d;

		// 실수형
		double e;
		float f;

		// 문자형
		char g;

		// 논리형
		boolean h;

		a = 100;
		b = 200;
		c = 300;
		d = 100;

		e = 0.0;
		f = 1.2f;

		g = 'a';

		h = true;

	}

}
